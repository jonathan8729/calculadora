class Operacao {
    
    public double somar(double n1, double n2){
    return n1+n2;
    }
    
    public double subitracao(double n1, double n2){
    return n1-n2;
    }
    
    public double multiplicacao(double n1, double n2){
    return n1*n2;
    }
    
    public double divisao(double n1, double n2){
    return n1/n2;
    }
    
}
